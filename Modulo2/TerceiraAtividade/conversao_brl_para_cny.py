# Definindo a quantidade inicial em reais (BRL)
quantia_brl = 100

# Definindo a taxa de câmbio de reais para yuan chineses (CNY)
taxa_cambio = 0.69

# Calculando o valor correspondente em yuan chineses (CNY)
valor_cny = quantia_brl / taxa_cambio

# Exibindo o resultado da conversão
print("Se você tem", quantia_brl, "reais, isso vale", valor_cny, "yuan chineses (CNY).")
