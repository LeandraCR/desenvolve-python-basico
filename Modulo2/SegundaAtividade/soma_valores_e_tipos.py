# Definindo uma variável de string que contém a mensagem
texto = "o resultado é"

# Definindo duas variáveis numéricas com diferentes tipos (inteiro e float)
valor1 = 10
valor2 = 3.5

# Realizando a soma das duas variáveis numéricas
resultado_soma = valor1 + valor2

# Exibindo a mensagem de texto
print(texto)

# Exibindo o tipo da variável 'texto' (deve ser 'str')
print(type(texto))

# Exibindo o valor da primeira variável 'valor1'
print(valor1)

# Exibindo o tipo da variável 'valor1' (deve ser 'int')
print(type(valor1))

# Exibindo o valor da segunda variável 'valor2'
print(valor2)

# Exibindo o tipo da variável 'valor2' (deve ser 'float')
print(type(valor2))

# Exibindo o resultado da soma
print(resultado_soma)

# Exibindo o tipo do resultado da soma (deve ser 'float' por causa da operação com o valor float)
print(type(resultado_soma))
