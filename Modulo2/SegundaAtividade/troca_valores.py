# Inicializamos duas variáveis, v1 e v2, com os valores 10 e 20, respectivamente.
v1 = 10
v2 = 20

# Armazenamos o valor de v1 na variável aux, para podermos trocar os valores de v1 e v2.
aux = v1

# Atribuímos o valor de v2 a v1, fazendo a troca.
v1 = v2

# Atribuímos o valor armazenado em aux (que era o valor original de v1) a v2.
v2 = aux

# Imprimimos o novo valor de v1 (que agora é 20).
print(v1)

# Imprimimos o novo valor de v2 (que agora é 10).
print(v2)
